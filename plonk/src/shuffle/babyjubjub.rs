#[test]
fn test_babyjubjub_remark() {
    use ark_ec::PrimeGroup;
    use ark_ed_on_bn254::{EdwardsAffine, EdwardsProjective, Fr};
    use ark_ff::UniformRand;
    use ark_std::rand::SeedableRng;
    use rand_chacha::ChaChaRng;

    use crate::shuffle::{BabyJubjubShuffle, Ciphertext, Remark};

    let mut prng = ChaChaRng::from_entropy();
    let secret = Fr::rand(&mut prng);
    let public = EdwardsProjective::generator() * secret;

    let m = EdwardsProjective::rand(&mut prng);
    let input = Ciphertext::encrypt(&mut prng, &m, &public);

    let bits = BabyJubjubShuffle::sample_random_scalar_bits(&mut prng);
    let remask_card = BabyJubjubShuffle::eval_remark(&input, &bits, &public);
    assert!(remask_card.verify(&m, &secret));

    let trace = BabyJubjubShuffle::eval_remark_with_trace(&input, &bits, &public);
    assert_eq!(trace.intermediate_values.len(), trace.n_round);

    let output = trace.output;
    let e2: EdwardsProjective = EdwardsAffine::new_unchecked(output[0], output[1]).into();
    let e1: EdwardsProjective = EdwardsAffine::new_unchecked(output[2], output[3]).into();
    let remask_trace = Ciphertext::new(e1, e2);
    assert_eq!(remask_card, remask_trace);
    assert!(remask_trace.verify(&m, &secret));
}
